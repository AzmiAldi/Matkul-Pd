# 2. MENGHITUNG MUNDUR KEBALIKANNYA DARI 1

# meminta pengguna memasukkan angka akhir
angka_awal = 1
angka_akhir = int(input("masukkan angka akhir: "))
#loop while untuk menghitung maju
